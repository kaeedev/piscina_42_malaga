#include <unistd.h>

void	ft_is_negative(int n)
{
	char	p;
	char	ng;

	if (n >= 0)
	{
		p = 'P';
		write(1, &p, 1);
	}
	else
	{
		ng = 'N';
		write(1, &ng, 1);
	}
}
